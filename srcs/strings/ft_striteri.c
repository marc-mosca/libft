#include "../../incs/libft.h"

void	ft_striteri( char *s, void (*f)(uint32_t, char *) )
{
	uint32_t	i;

	if (!s)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		f(i, &s[i]);
		i++;
	}
}
